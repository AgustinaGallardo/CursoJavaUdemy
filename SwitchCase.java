package ppp;

public class SwitchCase {

	public static void main (String[] args) {
	 
		String weader = "sunny";
		
		switch (weader){
		
		case "sunny" : 
			System.out.println("Esta soleado");
			break;	
			
		case "cloudy" : 
			System.out.println("Esta nublado");
			break;
			
		default : 
			System.out.println("No se encontro el clima");
			
		}
		
		
		
	}
}
